-- 함수 : 수학 

-- abs : 절대값
select abs(-1), abs(1.1);

-- mod : 나누기 나머지 출력?
select mod(10, 3);

-- floor
select floor(3.24);

-- ceil
select ceil(3.24);
select ceiling(3.24);

-- round(x) : x에 가장 근접한 정수
-- round(x, d) : x 값 중에 소수점 d 자리에 가장 근접한 실수
select round(1.28);
select round(1.54);
select round(1.498, 1),round(1.498),round(1.498, 0);



